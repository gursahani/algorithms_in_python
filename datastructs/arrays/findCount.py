from typing import List


class Solution:
    # @param a : tuple of integers
    # @param b : integer
    # @return an integer
    def binary_search(self, a, b, search_first: bool) -> int:
        left = 0
        right = len(a) - 1
        result = -1
        while left <= right:
            mid = (left + right) // 2
            if a[mid] == b:
                result = mid
                if search_first:
                    right = mid - 1
                elif not search_first:
                    left = mid + 1
            elif a[mid] < b:
                left = mid + 1
            elif a[mid] > b:
                right = mid - 1

        return result

    def findCount(self, a: List[int], b: int) -> int:
        first_index = self.binary_search(a, b, True)
        if first_index == -1:
            return 0
        else:
            last_index = self.binary_search(a, b, False)
        return last_index - first_index + 1


